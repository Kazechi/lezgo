    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        

        try{

        String fileName = JOptionPane.showInputDialog(null, "Input Your Surname (e.g (Somera.dat))");
        
        String directory = System.getProperty("user.dir");
        String filePath = directory + File.separator + fileName;
        
        
        FileWriter storeFile = new FileWriter(filePath);
        
            
        
        storeFile.write(txtName.getText());
        storeFile.write(txtTime.getText());
        storeFile.write(txtDate.getText());
        storeFile.write(txtSubject.getText());
        storeFile.close();
            
        
        JOptionPane.showMessageDialog(null, "Successfully Created!");
        

        
        }catch(Exception e){
                e.printStackTrace();
                }
        
    }//GEN-LAST:event_btnCreateActionPerformed

