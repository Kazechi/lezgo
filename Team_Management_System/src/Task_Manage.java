    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        String fileName = JOptionPane.showInputDialog(null, "Input the File Name (e.g (name.dat))");
        JOptionPane.showConfirmDialog(null,"Do you want to Delete this File?");
        
        String directory = System.getProperty("user.dir");
        String filePath = directory + File.separator + fileName;
        
        try{
        File delete = new File(filePath);
        if(delete.delete()){
            JOptionPane.showMessageDialog(null, "The File Has Been Deleted!");
        }else{
            JOptionPane.showMessageDialog(null, "No File Found!");
        }
        
        
        
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    
    
